numbers = (1,2,3) # tuple initialization
print(numbers)
print(type(numbers))
# changing values are not entertained in tuples like numbers[1] = 4
# tuples are immutable
player = tuple(('Name', 18, True)) #another way of initialising using tuple constructor same as like list
# len,type all work same like list operations
print(player)
print(type(player))
